# --- 1. 基本設定 (主にここを編集します) ---

# 作成するPDFのサイズを選びます ("A4" または "A5")
PAGE_SIZE = "A4"

# ▼ QUADERNOで表示する「Index Key」の選択肢です ▼
# ※Synapsen本体の config.ini 内 [CommonplaceKeys] の
# 　options = の内容と、必ず一致させてください。
OPTIONS = [
    'タスク',
    'アイデア',
    '思考・考察',
    'コミュニケーション',
    '学習・情報収集',
    '日常・その他'
]


# --- 2. デザイン・フォントの詳細設定 (変更しなくても動作します) ---

# ▼ PDFに埋め込むフォントの「絶対パス」を指定します ▼
# パスは「r'」で始まり、(フォルダの場所) + (ファイル名) で指定します。
#
# (推奨) Windows 標準の MSゴシック (このままでOK)
FONT_PATH = r'C:\windows\fonts\msgothic.ttc'
# (参考) PDF内部で使うフォント名 (FONT_PATHと合っていれば何でもOK)
FONT_NAME = "msgothic"

# (例) もし Noto Sans JP をPCにインストールしている場合
# FONT_PATH = r'C:\Windows\Fonts\NotoSansJP-VF.ttf'
# FONT_NAME = "noto-sans-jp"


# ▼ 出力するPDFテンプレートのファイル名です ▼
FILE_NAME = "DotLegalPad_Template"


# --- 3. テンプレートのレイアウト設定 (単位: cm) ---

# ページ上部の余白 (この範囲には罫線やドットが引かれません)
HEADER_POSITION = 1       # [cm]
# ページ下部の余白
FOOTER_POSITION = 0.5     # [cm]
# 左側の赤い縦線までの余白
LEFT_MARGIN_LINE = 4      # [cm]


# --- 4. その他のデザイン設定 ---

# ドット方眼の「点」の半径（大きさ）
DOT_READIUS = 1.5

# 「Index Key」を選ぶプルダウンメニューの横幅です
# (単位: ポイント。通常はこのままでOK)
FORM_WIDTH = 375

# 罫線やドットの色を (R, G, B) で指定します
# (0.0 = 真っ黒, 1.0 = 真っ白) の割合で指定してください
# (0.85, 0.85, 0.85) は「薄いグレー」です
COLOR_LINE = (0.85, 0.85, 0.85)
